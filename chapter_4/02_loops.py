
i = 1
while i<=10: 
    print("While i = ",i)
    i+=1


fruits = ["Banana", "Mango","Orange", "Greps", "Guava", "Water Melon"]
# while loop
i = 0
while i<len(fruits):
    print(fruits[i])
    i+=1


# for loop 
for fruit in fruits:
    print(fruit)
# for loop in range
l = 10
for i in range(0,l):
    print(i)

# add step size : 2
for i in range(0,l,2):
    print(i)